package Calculator;

public class Calculator {

        static double add (int a, int b){
             return a + b;
        }
    static double substract (int a, int b){
             return a - b;
        }

    static double multiply(int a, int b){
        return a * b;
    }
    static double divide (int a, int b){
        return a / b;

    }
}
